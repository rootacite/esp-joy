    gpio_num_t dc, rst;

    uint8_t _colstart = 2, _rowstart = 1, _tft_type = 0, _rotation = 0, _xstart = 0, _ystart = 0;
    uint8_t tft_width = 160, tft_height = 128;

    uint16_t* frame_buffer = nullptr;

    spi_device_handle_t spi2_handle = {};

private:
    void swap_byte(uint16_t *x);
    void write_command(uint8_t cmd);
    void write_data(uint8_t data);
    void write_block(uint8_t* data, uint32_t cc);
    void write_r_cmd_1();
    void write_r_cmd_2();
    void write_r_cmd_3();
    void setWindow(uint8_t x0, uint8_t y0, uint8_t x1, uint8_t y1);

